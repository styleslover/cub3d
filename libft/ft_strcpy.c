#include "libft.h"

char	*ft_strcpy(char *dest, char *src)
{
	size_t	a;

	a = 0;
	if (!dest || !src)
		return (0);
	while (src[a] != '\0')
	{
		dest[a] = src[a];
		a++;
	}
	dest[a] = '\0';
	return (dest);
}
